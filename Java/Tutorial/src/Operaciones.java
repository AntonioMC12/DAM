
public class Operaciones {

	public static void main(Bienvenido[] args) {
	
	//Deblaramos las variables para usarlas en las operacion que nos piden
		
	byte num1 = 9;
	byte num2 = 3;
	
	System.out.println("Los números con los que trabajamos son: " + num1 + ", "+ num2);
	
	//realizamos las operaciones pertinentes y las mostramos por consola
	System.out.println("El resultado de la suma es: " + (num1 + num2));
	System.out.println("El resultado de la resta es: " + (num1 - num2));
	System.out.println("El resultado de la multiplicación es: " + (num1 * num2));
	System.out.println("El resultado de la división es: " + (num1 / num2));
	}

}
